const messages: Record<Locale, Record<TranslationKey, string>> = {
  "zh-CN": zhCN,
  en,
};

let activeLocale: Locale = "zh-CN";

export function isLocale(value: unknown): value is Locale {
  return value === "zh-CN" || value === "en";
}

export function setActiveLocale(locale: Locale): void {
  activeLocale = locale;
}

export function getActiveLocale(): Locale {
  return activeLocale;
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  values: TranslationValues = {},
): string {
  return messages[locale][key].replace(/\{(\w+)\}/g, (match, name) =>
    name in values ? String(values[name]) : match,
  );
}

export function translateCurrent(
  key: TranslationKey,
  values?: TranslationValues,
): string {
  return translate(activeLocale, key, values);
}

const API_ERROR_KEYS: Record<string, TranslationKey> = {
  already_initialized: "errors.alreadyInitialized",
  ai_not_configured: "errors.aiNotConfigured",
  invalid_credentials: "errors.invalidCredentials",
  invalid_field: "errors.invalidField",
  invalid_file: "errors.invalidFile",
  invalid_filename: "errors.invalidFilename",
  invalid_json: "errors.invalidRequest",
  key_unavailable: "errors.keyUnavailable",
  missing_file: "errors.missingFile",
  not_found: "errors.notFound",
  rate_limited: "errors.rateLimited",
  too_large: "errors.fileTooLarge",
  unauthenticated: "errors.unauthenticated",
  version_conflict: "errors.versionConflict",
};

const containsHan = (value: string) => /[\u3400-\u9fff]/.test(value);

export function localizeServerMessage(
  message: string | undefined,
  code?: string,
  fallback: TranslationKey = "errors.requestFailed",
): string {
  if (activeLocale === "zh-CN") {
    return message || translateCurrent(fallback);
  }
  const mapped = code ? API_ERROR_KEYS[code] : undefined;
  if (mapped) {
    return translateCurrent(mapped);
  }
  if (message && !containsHan(message)) {
    return message;
  }
  return translateCurrent(fallback);
}
